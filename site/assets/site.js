  const buttons = Array.from(document.querySelectorAll("[data-lang]"));
  const requested = new URLSearchParams(window.location.search).get("lang");
  const saved = localStorage.getItem("html-vault-site-lang");
  const initial = dictionaries[requested] ? requested : dictionaries[saved] ? saved : "en";

  function applyLanguage(lang) {
    const dictionary = dictionaries[lang] || dictionaries.en;
    document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
    document.querySelectorAll("[data-i18n]").forEach((element) => {
      const value = dictionary[element.dataset.i18n];
      if (value) element.textContent = value;
    });
    document.querySelectorAll("[data-i18n-html]").forEach((element) => {
      const value = dictionary[element.dataset.i18nHtml];
      if (value) element.innerHTML = value;
    });
    buttons.forEach((button) => {
      button.classList.toggle("active", button.dataset.lang === lang);
      button.setAttribute("aria-pressed", button.dataset.lang === lang ? "true" : "false");
    });
    document.querySelectorAll("[data-demo-link]").forEach((link) => {
      const demoLang = lang === "zh" ? "zh-CN" : lang;
      link.href = `demo/?lang=${encodeURIComponent(demoLang)}`;
    });
    localStorage.setItem("html-vault-site-lang", lang);
  }

  buttons.forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.lang));
  });

  applyLanguage(initial);
